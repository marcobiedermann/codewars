import points from '.';

describe('points', () => {
  it("testing for points(['1:0','2:0','3:0','4:0','2:1','3:1','4:1','3:2','4:2','4:3'])", () => {
    expect.assertions(1);

    expect(
      points(['1:0', '2:0', '3:0', '4:0', '2:1', '3:1', '4:1', '3:2', '4:2', '4:3']),
    ).toStrictEqual(30);
  });

  it("testing for points(['1:1','2:2','3:3','4:4','2:2','3:3','4:4','3:3','4:4','4:4'])", () => {
    expect.assertions(1);

    expect(
      points(['1:1', '2:2', '3:3', '4:4', '2:2', '3:3', '4:4', '3:3', '4:4', '4:4']),
    ).toStrictEqual(10);
  });

  it("testing for points(['0:1','0:2','0:3','0:4','1:2','1:3','1:4','2:3','2:4','3:4'])", () => {
    expect.assertions(1);

    expect(
      points(['0:1', '0:2', '0:3', '0:4', '1:2', '1:3', '1:4', '2:3', '2:4', '3:4']),
    ).toStrictEqual(0);
  });

  it("testing for points(['1:0','2:0','3:0','4:0','2:1','1:3','1:4','2:3','2:4','3:4'])", () => {
    expect.assertions(1);

    expect(
      points(['1:0', '2:0', '3:0', '4:0', '2:1', '1:3', '1:4', '2:3', '2:4', '3:4']),
    ).toStrictEqual(15);
  });

  it("testing for points(['1:0','2:0','3:0','4:4','2:2','3:3','1:4','2:3','2:4','3:4'])", () => {
    expect.assertions(1);

    expect(
      points(['1:0', '2:0', '3:0', '4:4', '2:2', '3:3', '1:4', '2:3', '2:4', '3:4']),
    ).toStrictEqual(12);
  });
});
